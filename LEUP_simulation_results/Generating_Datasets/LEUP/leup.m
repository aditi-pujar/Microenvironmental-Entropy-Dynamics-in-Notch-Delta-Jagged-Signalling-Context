function L=leup(L1,L2,L3,beta,N,dt)
delL_num=(N-1)*L1/N^2-L2/(N)^2;
delL_den=(N-1)*L1^2/N^2+L3/N-(L2)^2/(N)^2-2*L1*L2/(N)^2;
delL=delL_num/delL_den;

L=L1+beta*delL*dt;
end
